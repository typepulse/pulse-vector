CREATE OR REPLACE FUNCTION "public"."get_recent_user_activity"() RETURNS TABLE("user_id" "uuid", "signup_date" "date", "api_key_created" boolean, "file_uploaded" boolean)
    LANGUAGE "sql" STABLE SECURITY DEFINER
    SET "search_path" TO 'auth', 'public'
    AS $$
    SELECT 
        u.id AS user_id,
        DATE(u.created_at AT TIME ZONE 'UTC') AS signup_date,
        CASE 
            WHEN a.id IS NOT NULL THEN true
            ELSE false
        END AS api_key_created,
        CASE 
            WHEN f.id IS NOT NULL THEN true
            ELSE false
        END AS file_uploaded
    FROM 
        public.profiles u
    LEFT JOIN 
        public.api_keys a ON u.id = a.user_id
    LEFT join
        public.team_memberships tm on u.id = tm.profile_id
    LEFT JOIN 
        public.files f ON tm.team_id = f.team_id
    WHERE 
        u.created_at >= NOW() - INTERVAL '7 days'
    GROUP BY 
        u.id, signup_date, a.id, f.id
    ORDER BY 
        signup_date;
$$;


